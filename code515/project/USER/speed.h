#ifndef __SPEED_H
#define __SPEED_H

/*==========================================================================
 * 速度与阈值配置文件
 * 所有可调参数集中在此，无需深入 ai.c / main.c
 *
 * PWM占空比范围: 0 ~ 899 (TIM2_PWM_Init(899,0))
 * 距离单位: 厘米 (cm)
 *==========================================================================*/

// ============================
// 电机 PWM 速度值（0~899）
// ============================
// 注意：值越大车轮转速越快。值过小(<100)可能扭矩不足无法起步。

/* 直行速度 — 用于无障碍时前进 */
#define SPEED_GO_L        150     // 左轮直行速度
#define SPEED_GO_R        150     // 右轮直行速度

/* 转弯速度 — 用于避障转向 */
#define SPEED_TURN_L      150     // 左转时左轮速度
#define SPEED_TURN_R      150     // 左转时右轮速度

/* 后退速度 — 用于紧急后退 */
#define SPEED_BACK_L      140     // 后退时左轮速度
#define SPEED_BACK_R      140     // 后退时右轮速度

// ============================
// 避障距离阈值（厘米）
// ============================

/* 紧急后退 — 距离小于此值时立刻后退，防撞 */
#define THR_HEAD_EMRG     8       // 正前方紧急距离
#define THR_SIDE_EMRG     5       // 左右侧紧急距离

/* 普通避障 — 距离小于此值时触发转向 */
#define THR_HEAD_NEAR     20      // 前方障碍距离
#define THR_LEFT_NEAR     12      // 左侧障碍距离
#define THR_RIGHT_NEAR    12      // 右侧障碍距离

/* 弯道检测 — 前方中等距离 + 左侧非常开阔，可能遇到左弯道 */
#define THR_CURVE_HEAD_LO 25      // 弯道判断-前方最小距离
#define THR_CURVE_HEAD_HI 45      // 弯道判断-前方最大距离
#define THR_CURVE_LEFT     120    // 弯道判断-左侧开阔阈值（较大值）

// ============================
// 出口检测阈值（厘米）
// 原理: 45°安装的左右超声，距离先降后升，上升跃变=驶出出口
// ============================

#define EXIT_SIDE_LOW     60      // 两侧低于此值视为"进入通道"
#define EXIT_SIDE_HIGH    80      // 两侧高于此值视为"已离开"(回状态0)
#define EXIT_JUMP_DELTA   30      // 两侧同时跃升超过此值=确认驶出
#define EXIT_HEAD_MIN     25      // 前方必须大于此值才参与判断(排除墙角)
#define EXIT_STABLE_CNT   3       // 连续低位确认次数(入通道防抖)
#define EXIT_COOLDOWN     40      // 触发后冷却周期数(防重复)

// ============================
// 延时参数（毫秒）
// ============================

#define DELAY_EMRG        50      // 紧急后退持续时间
#define DELAY_TURN        50      // 避障转向持续时间
#define DELAY_CURVE       80      // 弯道左转探索持续时间

#endif
